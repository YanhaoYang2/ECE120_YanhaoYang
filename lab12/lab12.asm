        LDI  R3,ValueP       ;Store the ASCII code of the required character to R3;
        LDI  R5,ValueZ       ;Store the value that will be printed if it is 0;
	LDI  R6,ValueO       ;Store the value that will be printed if it is 1;
        LEA R2,FIRST         ;Assign R2 first to the location of start of FONT_DARA
        ADD R3, R3, #0       ; Test if R3 equal 0 if it is 0, jump the Find address process
        BRz Jump
FindADD ADD R2, R2, #8       ;ADD 16 to R2 by addding twice 8
        ADD R2, R2, #8
        ADD R3, R3, #-1      ;loop counter -1
        BRp FINDADD         ;Back to start of the loop until counter down to 0         
Jump    ADD R1, R2, #0      ;Store the first line of the FONT_DATA to R1
        AND R2, R2, #0       ;Clear R2;
	ADD R2, R2, #16      ;R2 reassigned as Row Counter, set to 16
NEXTROW ADD R2, R2, #0       ;Set cc
        BRz DONE             ;if R2 is already zero, end the program
	AND R3, R3, #0       ;Clear R3, that is assigned as Column Counter
	ADD R3, R3, #8       ;Set Column Counter to 8;
        LDR R4, R1, #0       ;Store the value of the line of font DATA to R4;
NEXTCOLUMN ADD R3, R3, #0    ;Determine if R3 is 0
	BRz DONEROW         ;If R3 is zero, goto nextrow
        ADD R4, R4,#0        ; Try if the next bit to read is 1 or 0;
        BRn PRINTONE        ; if negative, the next bit is one 
        ADD R0, R5, #0       ; if not, print 0 represented value;
        OUT
        ADD R4, R4, R4      ; leftshit R4
        ADD R3, R3, #-1     ; Decrease column counter by 1
        BRnzp NEXTCOLUMN     ;Back
PRINTONE ADD R0, R6, #0      ; negative, first value is 1, print ASCII at X5001;
        OUT
        ADD R4, R4, R4       ; Leftshift R4, test next value;
        ADD R3, R3, #-1       ; Decrease column counter and move to next value;
        BRnzp NEXTCOLUMN     ; Back
DONEROW ADD R1, R1, #1;      ; R1 point to next line in FONT_DATA
        LD R0, ASCII_NL      ; Move to next line in monitor;
	OUT
        ADD R2, R2, #-1
        BRnzp NEXTROW
