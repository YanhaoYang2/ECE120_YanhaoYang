;R0:Stored the ASCII code of the element that is ready to be printed 
;R1:The location of each line of FONT_DATA
;R2:First used as the register that finds the first line of the FONT_DATA, then used as Row counter
;R3: First Used to contain the ASCII code of the requried character in x5002, then used as the column counter
;R4: cotainer for content ofeach line of the FONT_DATA that is going to be analyzed
;R5: contains the ASCII element that is to be printed if the bit that is analyzed is 0
;R6: contains the ASCII element that is to be printed if the bit that is analyzed is 1
.ORIG x3000; 
	AND R1, R1, #0       ;Clear R1;
	AND R2, R2, #0       ;Clear R2, the regiser used to find the address of the first line of font data;
