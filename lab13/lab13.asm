;R0:First used to point the location of the character that is being analyzed, then Stored the ASCII code of the element that is ready to be printed 
;R1:The location of each line of FONT_DATA
;R2:Row counter
;R3: First Used to contain the content of ASCII code of the requried character in x5002 and others following it, then used as the column counter
;R4: cotainer for content of each line of the FONT_DATA that is going to be analyzed
;R5: Character Counter
;R6: Row coutner counting upwards
.ORIG x3000; 
	AND R2, R2, #0       ;Clear R2, the Row counter
	ADD R2, R2, #8	     ;
	ADD R2, R2, #8       ;R2 reassigned as Row Counter, set to 16
	AND R6, R6 ,#0       ; R6 as another Row counter, coutnering upwards
        ADD R6, R6 ,#-1      ; First initialize R6 to -1 , so it is 0 at the first loop for every line
NEXTLINE ADD R6, R6, #1      ; Pointing to next Row
        AND R5, R5,#0        ; R5 assigned as Char Counter;
	ADD R5, R5,#-1       ; Make R5 set to -1 so that it could be zero in the first loop for every character
NEXTCHAR LD R0, ValueP      ;
	ADD R5,R5, #1        ; Add R5 each loop by 1
	ADD R0, R0, R5       ; R0 points in the location of the character that is being analyzed
        LDR R3, R0, #0     ;Store the ASCII code of the required character to R3;
        LEA R4,FONT_DATA     ;Assign R4 first to the location of start of FONT_DARA
        ADD R3, R3, #0       ; Test if R3 equal 0 if it is 0, 
        BRz TEST             ;TEST it if ROW counter has reached zero, if R3 is zero
FindADD ADD R4, R4, #8       ;ADD 16 to R2 by addding twice 8
        ADD R4, R4, #8
        ADD R3, R3, #-1      ;loop counter -1
        BRp FINDADD         ;Back to start of the loop until counter down to 0 
	ADD R4, R4, R6       ; points to the row of the current char that is being analyzed    
	AND R1, R1, #0    
        ADD R1, R4, #0      ;Store the line of the FONT_DATA to R1
        AND R4, R4, #0       ;Clear R4;
	AND R3, R3, #0       ;Clear R3, that is assigned as Column Counter
	ADD R3, R3, #8       ;Set Column Counter to 8;
        LDR R4, R1, #0       ;Store the value of the line of font DATA to R4;
NEXTCOLUMN ADD R3, R3, #0    ;Determine if R3 is 0
	BRz NEXTCHAR         ;If R3 is zero, goto nextchar
        ADD R4, R4,#0        ; Try if the next bit to read is 1 or 0;
        BRn PRINTONE        ; if negative, the next bit is one 
        LDI  R0,ValueZ     ; if not, print 0 represented value;
        OUT
        ADD R4, R4, R4      ; leftshit R4
        ADD R3, R3, #-1     ; Decrease column counter by 1
        BRnzp NEXTCOLUMN     ;Back
PRINTONE LDI  R0,ValueO      ; negative, first value is 1, print ASCII at X5001;
        OUT
        ADD R4, R4, R4       ; Leftshift R4, test next value;
        ADD R3, R3, #-1       ; Decrease column counter and move to next value;
        BRnzp NEXTCOLUMN     ; Back
DONEROW 
        LD R0, ASCII_NL      ; Move to next line in monitor;
	OUT
        ADD R2, R2, #-1
        BRnzp NEXTLINE
TEST    ADD R2,R2, #0       ; if the Row counter has reached zero, end the program, otherwise, go back to the DONEROW
